/// Default border radius in App UI
abstract class AppBorderRadius {
  /// Default unit of border radius
  static const double borderRadiusUnit = 8;

  /// xxs border radius (1dp)
  static const double xxs = borderRadiusUnit * 0.125;

  /// xs border radius (2dp)
  static const double xs = borderRadiusUnit * 0.25;

  /// sm border radius (4dp)
  static const double sm = borderRadiusUnit * 0.5;

  /// md border radius (8dp)
  static const double md = borderRadiusUnit;

  /// lg border radius (16dp)
  static const double lg = borderRadiusUnit * 2;

  /// xlg border radius (24dp)
  static const double xlg = borderRadiusUnit * 3;

  /// xxlg border radius (32dp)
  static const double xxlg = borderRadiusUnit * 4;

  /// xxxlg border radius (40dp)
  static const double xxxlg = borderRadiusUnit * 5;
}
